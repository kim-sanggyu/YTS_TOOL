-- HWP 업로드 (귀속연도+사용자 1건, 재업로드 시 덮어쓰기)
CREATE TABLE MLAY_HWP_FILE (
  YEAR          NUMBER(4)       NOT NULL,
  USER_ID       NUMBER          NOT NULL,
  HWP_FILE_NAME VARCHAR2(200),
  HWP_FILE_PATH VARCHAR2(500),
  HWP_DATA      BLOB,
  ROW_COUNT     NUMBER          DEFAULT 0,
  UPLOADED_AT   DATE            DEFAULT SYSDATE NOT NULL,
  NOTE          VARCHAR2(200),
  CONSTRAINT PK_MLAY_HWP_FILE      PRIMARY KEY (YEAR, USER_ID),
  CONSTRAINT FK_MLAY_HWP_FILE_USER FOREIGN KEY (USER_ID) REFERENCES YTTS_USERS (USER_ID)
);

COMMENT ON TABLE  MLAY_HWP_FILE               IS '국세청 전산매체 HWP 업로드 (귀속연도+사용자 1건, 재업로드 시 덮어쓰기)';
COMMENT ON COLUMN MLAY_HWP_FILE.YEAR          IS '귀속연도 (예: 2025)';
COMMENT ON COLUMN MLAY_HWP_FILE.USER_ID       IS '업로드한 사용자 (YTTS_USERS.USER_ID)';
COMMENT ON COLUMN MLAY_HWP_FILE.HWP_FILE_NAME IS '업로드한 HWP 파일명';
COMMENT ON COLUMN MLAY_HWP_FILE.HWP_FILE_PATH IS '업로드한 HWP 파일 경로';
COMMENT ON COLUMN MLAY_HWP_FILE.HWP_DATA      IS 'HWP 파일 원본 (재파싱용)';
COMMENT ON COLUMN MLAY_HWP_FILE.ROW_COUNT     IS '파싱된 전체 항목 수';
COMMENT ON COLUMN MLAY_HWP_FILE.UPLOADED_AT   IS '업로드 일시';
COMMENT ON COLUMN MLAY_HWP_FILE.NOTE          IS '비고';

-- Java 소스 업로드 (귀속연도+사용자 1건, 재업로드 시 덮어쓰기)
-- MLAY_HWP_FILE와 독립적 — 입력 순서 무관, 상호 재업로드 영향 없음
CREATE TABLE MLAY_JAVA_FILE (
